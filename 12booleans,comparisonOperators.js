//  ==  Vs  ===  (equal too)
//  == will only check the value, not the datatype.
let num3= "10";
let num4= 10;
console.log(num3 == num4); //it returns true because the value is equal.

// === will check both, the value and the datatype,
let num5= "5";
let num6= 5;
console.log(num5 === num6);


//  !=  Vs  !== (notequalto)

//  != will only check the value, not the datatype.
let num7= 9;
let num8= 9;
console.log(num7 != num8);


//  !== will only check the value, not the datatype.
let num9= "10";
let num10= 10;
console.log(num9 !== num10);
